# Cria uma lista com sete elementos.

minha_lista = ['alfa','beta','teta','gama','sigma','delta','iota']

# Mostra os elementos da lista.

print(minha_lista)

# Conta o número de elementos da lista.

print(len(minha_lista))

# A compilação vai retornar 7, que é o número de elementos da minha lista.


# Em Python, podemos facilmente adicionar e retirar elementos de uma lista testando os comandos, veja como é simples, você pode replicar estes passos em seu terminal:

# crie uma lista com dois elementos numéricos.

lista=[8,9]

# adicione um novo elemento numérico à lista.

lista.append(10)

# mostre a nova lista.

print(lista)

# resultado.
# [8,9,10]

