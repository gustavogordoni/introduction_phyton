# Regras para nomear variáveis
# a) Os nomes das variáveis só podem conter letras (a-z, A-Z), números (0-9) e sublinhados (_). Não podem começar com um número.
# b) O Python diferencia maiúsculas de minúsculas, então nome e Nome são variáveis diferentes.
# c) Não se pode usar palavras-chave reservadas do Python como nomes de variáveis (por exemplo, if, else, for, while, etc.).

# Exemplos de nomes de variáveis válidos:
idade
nome_completo
total_vendas
_contador

# Exemplos de nomes de variáveis inválidos são:

# 1idade   # Começa com um número
# nome-completo   # Usa um hífen em vez de um sublinhado
# if   # Palavra-chave reservada do Python